#include "libft.h"

char	*ft_strrev(char *s)
{
	int		i;
	int		l;
	char	t;

	i = 0;
	l = ft_strlen(s);
	while (i < l)
	{
		t = s[i];
		s[i] = s[l];
		s[l] = t;
	}
	return (s);
}
