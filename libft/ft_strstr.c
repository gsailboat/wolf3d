#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int		i;
	int		l;
	int		j;

	i = 0;
	j = 0;
	if (s2[0] == '\0')
		return ((char*)s1);
	while (s1[i] != '\0')
	{
		j = i;
		l = 0;
		while (s1[j] == s2[l])
		{
			j++;
			l++;
			if (s2[l] == '\0')
				return (char*)(&s1[i]);
		}
		i++;
	}
	return (NULL);
}
