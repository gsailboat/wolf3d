#include "libft.h"

int		ft_power(int nb, int power)
{
	int res;

	res = 0;
	if (power < 0)
		return (0);
	else if (power == 0)
		return (1);
	else
	{
		while (power > 0)
		{
			res = res * nb;
			power--;
		}
	}
	return (res);
}
